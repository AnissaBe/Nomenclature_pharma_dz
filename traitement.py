def creer_resume(df):
    resume = df.groupby([
        "DENOMINATION COMMUNE INTERNATIONALE",
        "DOSAGE",
        "FORME_STD"
    ], dropna=False).agg(
        COUNT=("FORME_STD", "size"),
        LABORATOIRES=("LABORATOIRES DETENTEUR DE LA DECISION D'ENREGISTREMENT",
                      lambda x: ", ".join(x.dropna().unique()))
    ).reset_index()

    resume["R"] = "R" + resume["COUNT"].astype(str)
    resume = resume.drop(columns=["COUNT"])
    resume = resume.sort_values([
        "DENOMINATION COMMUNE INTERNATIONALE",
        "DOSAGE",
        "FORME_STD"
    ]).reset_index(drop=True)
    return resume