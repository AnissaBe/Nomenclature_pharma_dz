def clean_dosage(s):
    if pd.isna(s):
        return s
    s = str(s).strip().upper()
    unites = r'(MG/ML|G/ML|MG/G|MG/L|UI/ML|UI/MG|MCG/ML|MG/CM2|MMOL/ML|MEQ/ML|MG|MCG|UI|GR|ML|G|%)'
    s = re.sub(r'(\d[\d,\.]*)\s+' + unites, r'\1\2', s)
    s = re.sub(r'\(\s+', '(', s)
    s = re.sub(r'\s+\)', ')', s)
    s = re.sub(r'\s+', ' ', s).strip()
    return s


