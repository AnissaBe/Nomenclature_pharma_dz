def traiter_fichier(uploaded_file):
    """
    Prend un fichier uploadé via Streamlit et retourne :
    - df      : le dataframe complet avec FORME_STD
    - resume  : le tableau résumé avec colonne R
    """

    # ── Chargement ───────────────────────────────────────────────
    try:
        df = pd.read_csv(
            uploaded_file,
            sep=None,
            engine="python",
            encoding="utf-8"
        )
    except UnicodeDecodeError:
        uploaded_file.seek(0)
        df = pd.read_csv(
            uploaded_file,
            sep=None,
            engine="python",
            encoding="latin-1"
        )

    # ── Étape 0 : Normalisation Unicode ──────────────────────────
    df["FORME"] = df["FORME"].apply(normalize_unicode)

    # ── Étape 1 : Normalisation FORME_STD ────────────────────────
    df["FORME_STD"] = df.apply(normaliser, axis=1)

    # ── Étape 2 : Corrections ciblées par DCI ────────────────────
    df = corrections_ciblees(df)

    # ── Étape 3 : Nettoyage dosages ──────────────────────────────
    df["DOSAGE"] = df["DOSAGE"].apply(clean_dosage)

    # ── Étape 4 : Tableau résumé ─────────────────────────────────
    resume = creer_resume(df)

    return df, resume


# ═══════════════════════════════════════════════════════════════════
# ÉTAPE 0 — Normalisation Unicode
# ═══════════════════════════════════════════════════════════════════

def normalize_unicode(s):
    if pd.isna(s):
        return s
    s = str(s)
    s = s.replace('\u2019', "'").replace('\u2018', "'").replace('\u02BC', "'")
    s = s.replace('\u00AB', '"').replace('\u00BB', '"')
    s = s.replace('\u00A0', ' ').replace('\u202F', ' ')
    s = s.replace('à', 'a').replace('À', 'A').replace('é', 'e').replace('É', 'E')
    s = s.replace('è', 'e').replace('È', 'E').replace('ê', 'e').replace('Ê', 'E')
    s = s.replace('ô', 'o').replace('Ô', 'O').replace('û', 'u').replace('Û', 'U')
    s = s.replace('î', 'i').replace('Î', 'I').replace('ç', 'c').replace('Ç', 'C')
    s = s.replace('ù', 'u').replace('Ù', 'U').replace('â', 'a').replace('Â', 'A')
    return s


